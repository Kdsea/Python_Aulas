'''
Python = Linguagem de programação

Tipo de tipagem = Dinâmica  /  Forte, o 
python já sabe o tipo da informação que você está passando para ele

str -> string -> texto
Strings são textos que estão dentro de aspas
'''

print(1234)

# Aspas Simples
print('Kaua de Souza')
print(1, 'Kaua de "Souza"')

# Aspas Duplas
print("Kaua de Souza")
print(2, "Kaua de 'Souza'")

# Escape
print("Kaua de \"Souza\"")  # \ -> barra invertida

# r
print(r"Kaua de \"Souza\"")  # r -> raw string, não faz o escape

