# Tipos int e float

# int -> Número inteiro
# O tipo int representa qualuer número inteiro, positivo ou negativo. Int sem sinal é considerado
# Positivo
# print(11) # int
# print(-11) # int
# print(0) # int

# float -> Número com ponto flutuante.
# O tipo float representa quaquer número.
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
# print(1.1, 10.11) # float
# print(0.0, -1.5) # float

# a funçao type() mostra o tipo que o python
# interferiu ao valor
print(type('Kaua'))
print(type(1))
print(type(-1))
print(type(1.1), type(-1.1), type(0.0))	